# bgT5Rework
A reworked version of bgt5launcher v0.1.1 (with 0.7 planned) with significantly improved compatibility and UI

Bgt5launcher alpha v0.1.1 is unstable and unreliable, and 0.7 was never much better due to constant crashes. This project works to fix that.

NOTE: Be SURE to use this as a replacement exe in the bgt5launcher 0.1.1 patch. This standalone exe is not enough unless you already have bgt5launcher installed!
